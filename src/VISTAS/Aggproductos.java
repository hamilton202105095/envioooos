    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
       String Nombrepro = Txtnomp.getText();
    String PrepText = Txtprep.getText();

    if (Nombrepro.isEmpty() || PrepText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
    } else {
        try {
            double Codigopro = Double.parseDouble(PrepText);
            Producto producto = new Producto(Nombrepro, Codigopro);
            Tienda.getProductos().add(producto);
            
            
            
            JOptionPane.showMessageDialog(null, "Ingresó el producto exitosamente");
            
            // Limpiar los campos de texto
            Txtnomp.setText("");
            Txtprep.setText("");
            
            FileOutputStream fos = new FileOutputStream("C:\\Users\\herme\\OneDrive\\Documentos\\NetBeansProjects\\PRAACTICAA2\\productos.dat");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(Tienda.getProductos());
            fos.close();
            oos.close();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio debe ser un número válido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

